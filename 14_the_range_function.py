#The Range Function

for number in range(5):
    print(number)

numbers = range(5)
print("from 0 to 4")
for number in numbers:
    print(number)

numbers = range(5,10)
print("from 5 to 9")
for number in numbers:
    print(number)

numbers = range(5,10,2)
print("from 5 + 2 + 2")
for number in numbers:
    print(number)